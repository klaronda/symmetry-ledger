import { Helmet } from 'react-helmet-async';

interface SEOProps {
  title?: string;
  description?: string;
  path?: string;
}

export function SEO({ 
  title = "Symmetry Ledger - Reliable Bookkeeping",
  description = "Clean, accurate books. Delivered consistently. Professional bookkeeping services with over 10 years of experience in forensic auditing and financial analysis.",
  path = ""
}: SEOProps) {
  const url = `https://symmetryledger.com${path}`;
  const ogImage = "https://eexzqlaiymzwvyofztva.supabase.co/storage/v1/object/public/site-assets/backend/OpenGraph.webp";

  return (
    <Helmet>
      {/* Primary Meta Tags */}
      <title>{title}</title>
      <meta name="title" content={title} />
      <meta name="description" content={description} />

      {/* Open Graph / Facebook */}
      <meta property="og:type" content="website" />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={ogImage} />

      {/* Twitter */}
      <meta name="twitter:card" content="summary_large_image" />
      <meta property="twitter:domain" content="symmetryledger.com" />
      <meta property="twitter:url" content={url} />
      <meta name="twitter:title" content={title} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={ogImage} />

      {/* Canonical URL */}
      <link rel="canonical" href={url} />
    </Helmet>
  );
}
